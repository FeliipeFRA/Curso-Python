n1 = int(input('- Valor 1: '))
n2 = int(input('- Valor 2: '))
s = n1 + n2
su = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
r = n1 % n2
print(f'O resultado da soma entre os dois valores é: {s}')
print(f'O resultado da subtração entre os dois valores é: {su}')
print(f'O resultado da multiplicação entre os dois valores é: {m}')
print(f'O resultado da divisão entre os dois valores é: {d}, ',end='')
print(f'e seu resto é: {r}')
print(f'O resultado da divisão inteira entre os dois valores é: {di}')
